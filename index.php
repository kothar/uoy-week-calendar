  <h1>University of York (UK) Week Numbers</h1>
  <p>
    This page provides an iCal format description of the University of York week numbers, according to the university <a href="http://www.york.ac.uk/about/term-dates/">term dates calendar</a>. It is compatible with Google Calendar, and can simply be added to your view by subscribing to the following URL:
  </p>
  <blockquote>
    <a href="http://schmeeky.co.uk/weeks/ical.php">http://schmeeky.co.uk/weeks/ical.php</a>
    <br><br>
    <a href="http://www.google.com/calendar/render?cid=http%3A%2F%2Fschmeeky.co.uk%2Fweeks%2Fical.php" target="_blank"><img src="//www.google.com/calendar/images/ext/gc_button6.gif"></a>
  </blockquote>
  <p>
    For week numbers for the whole year, see <a href="http://sites.google.com/site/gcalweeknumbers/">http://sites.google.com/site/gcalweeknumbers/</a>. This will show the week numbers as blue circles (as shown below) and was the basis for this calendar.
  </p>
  <p>
    Google also have their own week numbers under Add -> Browse Interesting Calendars -> More.
  </p>
